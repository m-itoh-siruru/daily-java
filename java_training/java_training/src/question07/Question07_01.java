package question07;

public class Question07_01 {

	public static void main(String[] args) {
		int a1 = 12 + 3;
		int b2 = 12 - 3;
		int c3 = 12 * 3;
		int d4 = 12 / 3;
		int e5 = 4 % 3;
		
		System.out.println("12+3は" + a1 + "です");
		System.out.println("12-3は" + b2 + "です");
		System.out.println("12×3は" + c3 + "です");
		System.out.println("12÷3は" + d4 + "です");
		System.out.println("4÷3の余りは" + e5 + "です");

	}

}
