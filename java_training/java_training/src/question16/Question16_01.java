package question16;

public class Question16_01 {
	// 引数なしコンストラクタ
	public Question16_01() {
		System.out.println("コンストラクタです");
	}

	// int 型の引数を持つコンストラクタ
	public Question16_01(int num) {
		System.out.println("数値を受け取ったコンストラクタ: " + num);
	}

	// String 型の引数を持つコンストラクタ
	public Question16_01(String name) {
		System.out.println("文字列を受け取ったコンストラクタ: " + name);
	}
}
