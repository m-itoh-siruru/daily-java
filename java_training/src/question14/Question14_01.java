package question14;

// クラス作成
public class Question14_01 {
	// フィールド
	private int num;
	private double gas;

	// メソッドを定義
	public void setNum(int num) {
		this.num = num;
	}
	
	public void setGas(double gas) {
		this.gas = gas;
	}

	public int getNum() {
		return num;
	}

	public double getGas() {
		return gas;
	}
}