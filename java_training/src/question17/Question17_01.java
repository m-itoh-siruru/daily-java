package question17;

// クラス作成
public class Question17_01 {
	// フィールド
	static int num;
	
	// メソッド 処理
	static void message() {
		System.out.println("メッセージを表示します");
		System.out.println("num の初期値は" + num + "です");
	}
	
	// main メソッド（プログラムの入り口から処理がスタート）
	public static void main(String[] args) {
		message();
	}
}